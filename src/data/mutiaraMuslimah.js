/**
 * Mendapatkan mutiara acak berdasarkan tanggal hari ini (stabil sepanjang hari)
 * @returns {string} satu mutiara muslimah
 */
export function getMutiaraHarian() {
  const now = new Date()
  // Seed: tahun × 1000 + hari ke-N dalam tahun → unik setiap hari
  const start = new Date(now.getFullYear(), 0, 0)
  const diff = now - start
  const oneDay = 1000 * 60 * 60 * 24
  const dayOfYear = Math.floor(diff / oneDay)
  const seed = now.getFullYear() * 1000 + dayOfYear

  // LCG seeded random agar konsisten per hari
  const x = Math.sin(seed + 1) * 10000
  const rand = x - Math.floor(x)
  return MUTIARA_MUSLIMAH[Math.floor(rand * MUTIARA_MUSLIMAH.length)]
}
